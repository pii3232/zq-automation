# 直接测试哪种方式能在三引号字符串中正确表示字面的三引号

# 方式1: 使用 \"\"\"
test1 = """def foo():
    \"""docstring\"""
    pass"""
print(f'方式1成功:')
print(test1)
print()

# 方式2: 使用 \\"""  - 这会导致问题
# test2 = """def foo():
#     \\"""

# 方式3: 使用 \\""""
# 这也不对，因为 \\ 是一个反斜杠，后面是 """
# 所以 \\"""" 会被解析为: 反斜杠 + 反斜杠 + """
# 在字符串中，\\\\ 是一个反斜杠字符
# 所以结果是: \ + """
# 这会导致字符串提前结束！

# 正确的方式是: \"\"\"
# \" 转义第一个引号，后面两个引号是普通引号
# 这样 \"\"\\" 会被解析为: \" (转义的引号) + \" (普通引号) + \" (普通引号) = 字面的三引号

# 但是！在三引号字符串中，\"\"\\" 仍然会被解析为字符串结束！
# 因为 \"\"\\" 中，\"\"\\" 匹配了 \"\"\\" 模式，Python会认为这是: \" + \"\" = 结束

# 让我验证
test3 = """def foo():
    \\"\""docstring\\"\""
    pass"""
print(f'方式3成功:')
print(test3)
